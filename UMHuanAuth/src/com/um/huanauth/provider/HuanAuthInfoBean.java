package com.um.huanauth.provider;

public class HuanAuthInfoBean {
	
	private String mDeviceid = "";
	private String mDnum = "";
	private String mDevicemode = "";
	private String mActivekey = "";
	private String mDidtoken = "";
	private String mToken = "";
	private String mHuanid = "";
	private String mLicensetype = "";
	private String mLicensedata = "";
	
	public void setDeviceid(String deviceid){
		mDeviceid = deviceid;
	}
	
	public String getDeviceid(){
		return mDeviceid;
	}
	
	public void setDnum(String dnum){
		mDnum = dnum;
	}
	
	public String getDnum(){
		return mDnum;
	}
	
	public void setDevicemode(String devicemode){
		mDevicemode = devicemode;
	}
	
	public String getDevicemode(){
		return mDevicemode;
	}
	
	public void setActivekey(String activekey){
		mActivekey = activekey;
	}
	
	public String getActivekey(){
		return mActivekey;
	}
	
	public void setDidtoken(String didtoken){
		mDidtoken = didtoken;
	}
	
	public String getDidtoken(){
		return mDidtoken;
	}
	
	public void setToken(String token){
		mToken = token;
	}
	
	public String getToken(){
		return mToken;
	}
	
	public void setHuanid(String huanid){
		mHuanid = huanid;
	}
	
	public String getHuanid(){
		return mHuanid;
	}
	
	public void setLicensetype(String licensetype){
		mLicensetype = licensetype;
	}
	
	public String getLicensetype(){
		return mLicensetype;
	}
	
	
	public void setLicensedata(String licensedata){
		mLicensedata = licensedata;
	}
	
	public String getLicensedata(){
		return mLicensedata;
	}
}
